package com.it.academy;

public class NumberComparison {
    public static void main(String[] args)
    {
        byte a=25;
        byte b=39;
        if(a < b) {
            System.out.println(a + " > " + b);
        }
        byte c=100;
        byte d=100;
        if(c == d) {
            System.out.println(c + " = " + d);
        }
    }
}
